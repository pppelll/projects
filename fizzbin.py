for i in range(101):
    if i % 3 == 0 and i % 5 == 0:
        print(i)
        print('FizzBin!')
    elif i % 3 == 0 and i % 5 != 0:
        print(i)
        print('Fizz')
    elif i % 5 == 0 and i % 3 != 0:
        print(i)
        print('Bin')
    else:
        print(i)
        continue





